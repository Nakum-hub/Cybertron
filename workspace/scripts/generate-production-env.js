#!/usr/bin/env node

const crypto = require('node:crypto');
const fs = require('node:fs');
const path = require('node:path');

const workspaceRoot = path.resolve(__dirname, '..');
const defaultOutputPath = path.join(workspaceRoot, '.env');

function parseArgs(argv) {
  const args = {};

  for (let index = 0; index < argv.length; index += 1) {
    const token = String(argv[index] || '');
    if (!token.startsWith('--')) {
      continue;
    }

    const key = token.slice(2);
    const next = argv[index + 1];
    if (next && !String(next).startsWith('--')) {
      args[key] = String(next);
      index += 1;
      continue;
    }

    args[key] = 'true';
  }

  return args;
}

function usage() {
  return [
    'Usage:',
    '  node scripts/generate-production-env.js [options]',
    '',
    'Options:',
    '  --output <path>         Output env path (default: workspace/.env)',
    '  --domain <hostname>     Public Cybertron hostname, e.g. app.cybertronsecurity.com',
    '  --tls-email <email>     Email for ACME/TLS registration',
    '  --region <value>        Deployment region (default: us-east-1)',
    '  --db-host <host>        Managed PostgreSQL hostname',
    '  --db-port <port>        Managed PostgreSQL port (default: 5432)',
    '  --db-name <name>        PostgreSQL database name (default: cybertron)',
    '  --db-user <user>        PostgreSQL username (default: cybertron)',
    '  --redis-host <host>     Managed Redis hostname',
    '  --redis-port <port>     Managed Redis port (default: 6379)',
    '  --redis-scheme <name>   Redis scheme (default: rediss)',
    '  --s3-bucket <name>      S3 bucket for report storage',
    '  --s3-region <value>     S3 region (default: us-east-1)',
    '  --s3-endpoint <url>     Optional S3-compatible HTTPS endpoint',
    '  --llm-provider <name>   AI provider (default: openai)',
    '  --openai-base-url <url> OpenAI-compatible base URL (default: https://api.openai.com/v1)',
    '  --openai-model <name>   OpenAI-compatible model (default: gpt-4.1-mini)',
    '  --force                 Overwrite the output file if it already exists',
  ].join('\n');
}

function placeholder(label) {
  return `REPLACE_ME_${String(label || '').toUpperCase().replace(/[^A-Z0-9]+/g, '_')}`;
}

function randomHex(bytes) {
  return crypto.randomBytes(bytes).toString('hex');
}

function randomBase64(bytes) {
  return crypto.randomBytes(bytes).toString('base64');
}

function normalizeDomain(value) {
  return String(value || '')
    .trim()
    .replace(/^https?:\/\//i, '')
    .replace(/\/+$/, '');
}

function isNonEmpty(value) {
  return Boolean(String(value || '').trim());
}

function buildEnv(args) {
  const domain = normalizeDomain(args.domain);
  const frontendDomain = domain || placeholder('public_domain');
  const region = String(args.region || 'us-east-1').trim();
  const dbUser = String(args['db-user'] || 'cybertron').trim();
  const dbName = String(args['db-name'] || 'cybertron').trim();
  const dbHost = String(args['db-host'] || '').trim() || placeholder('managed_postgres_host');
  const dbPort = String(args['db-port'] || '5432').trim();
  const dbPassword = randomHex(24);
  const redisHost = String(args['redis-host'] || '').trim() || placeholder('managed_redis_host');
  const redisPort = String(args['redis-port'] || '6379').trim();
  const redisPassword = randomHex(24);
  const redisScheme = String(args['redis-scheme'] || 'rediss').trim().toLowerCase();
  const s3Bucket = String(args['s3-bucket'] || '').trim() || placeholder('reports_bucket');
  const s3Region = String(args['s3-region'] || region).trim();
  const s3Endpoint = String(args['s3-endpoint'] || '').trim();
  const llmProvider = String(args['llm-provider'] || 'openai').trim().toLowerCase();
  const tlsEmail = String(args['tls-email'] || '').trim() || placeholder('tls_email');
  const openaiBaseUrl = String(args['openai-base-url'] || 'https://api.openai.com/v1').trim();
  const openaiModel = String(args['openai-model'] || 'gpt-4.1-mini').trim();
  const openaiApiKey =
    llmProvider === 'openai' ? placeholder('openai_api_key') : '';
  const appVersionSuffix = domain ? '' : '-bootstrap';
  const frontendPort = '127.0.0.1:8080';

  return [
    '# Generated by scripts/generate-production-env.js',
    `# Generated at ${new Date().toISOString()}`,
    '# This file contains real rotated secrets for Cybertron bootstrap.',
    '# Replace every REPLACE_ME_* value with actual managed infrastructure values before internet launch.',
    '',
    'PORT=8001',
    'HOST=0.0.0.0',
    'NODE_ENV=production',
    `APP_VERSION=1.0.0${appVersionSuffix}`,
    `REGION=${region}`,
    `FRONTEND_PORT=${frontendPort}`,
    `FRONTEND_ORIGIN=https://${frontendDomain}`,
    `CORS_ALLOWED_ORIGINS=https://${frontendDomain}`,
    'LOCAL_PRODUCTION_VALIDATION=false',
    '',
    'AUTH_MODE=jwt_hs256',
    `JWT_SECRET=${randomBase64(48)}`,
    'JWT_ISSUER=cybertron',
    'JWT_AUDIENCE=cybertron-api',
    'JWT_CLOCK_SKEW_SECONDS=30',
    'AUTH_COOKIE_SECURE=true',
    'AUTH_COOKIE_SAMESITE=lax',
    `AUTH_COOKIE_DOMAIN=${domain || placeholder('auth_cookie_domain')}`,
    'CSRF_ENABLED=true',
    'ENFORCE_ORIGIN_VALIDATION=true',
    'ALLOW_INSECURE_DEMO_AUTH=false',
    'ALLOW_PUBLIC_REGISTRATION=false',
    'REQUIRE_AUTH_FOR_THREAT_ENDPOINTS=true',
    'REQUIRE_AUTH_FOR_PLATFORM_ENDPOINTS=true',
    'METRICS_REQUIRE_AUTH=true',
    `METRICS_AUTH_TOKEN=${randomHex(32)}`,
    'TRUST_PROXY=true',
    'STRICT_DEPENDENCIES=true',
    '',
    `POSTGRES_DB=${dbName}`,
    `POSTGRES_USER=${dbUser}`,
    `POSTGRES_PASSWORD=${dbPassword}`,
    `DATABASE_URL=postgresql://${encodeURIComponent(dbUser)}:${encodeURIComponent(dbPassword)}@${dbHost}:${dbPort}/${dbName}`,
    'DB_SSL_MODE=verify-full',
    '',
    `REDIS_PASSWORD=${redisPassword}`,
    `REDIS_URL=${redisScheme}://:${encodeURIComponent(redisPassword)}@${redisHost}:${redisPort}`,
    '',
    'REPORT_STORAGE_DRIVER=s3',
    `REPORT_STORAGE_S3_BUCKET=${s3Bucket}`,
    `REPORT_STORAGE_S3_REGION=${s3Region}`,
    `REPORT_STORAGE_S3_ENDPOINT=${s3Endpoint}`,
    `REPORT_STORAGE_S3_ACCESS_KEY_ID=${placeholder('s3_access_key_id')}`,
    `REPORT_STORAGE_S3_SECRET_ACCESS_KEY=${placeholder('s3_secret_access_key')}`,
    'REPORT_STORAGE_S3_FORCE_PATH_STYLE=false',
    '',
    `LLM_PROVIDER=${llmProvider}`,
    `OPENAI_API_KEY=${openaiApiKey}`,
    `OPENAI_BASE_URL=${llmProvider === 'openai' ? openaiBaseUrl : 'https://api.openai.com/v1'}`,
    `OPENAI_MODEL=${llmProvider === 'openai' ? openaiModel : 'gpt-4.1-mini'}`,
    '',
    '# Public edge / TLS',
    `CYBERTRON_DOMAIN=${frontendDomain}`,
    `CYBERTRON_TLS_EMAIL=${tlsEmail}`,
    'EDGE_HTTP_PORT=80',
    'EDGE_HTTPS_PORT=443',
    '',
    '# Optional external threat connectors',
    'WAZUH_API_URL=',
    'WAZUH_API_TOKEN=',
    'MISP_API_URL=',
    'MISP_API_KEY=',
    'OPENCTI_API_URL=',
    'OPENCTI_API_TOKEN=',
    'THEHIVE_API_URL=',
    'THEHIVE_API_TOKEN=',
    '',
    '# Optional enterprise SSO',
    'OIDC_ISSUER_URL=',
    'OIDC_CLIENT_ID=',
    'OIDC_CLIENT_SECRET=',
    '',
    '# Optional OAuth providers',
    'GOOGLE_CLIENT_ID=',
    'GOOGLE_CLIENT_SECRET=',
    'MICROSOFT_CLIENT_ID=',
    'MICROSOFT_CLIENT_SECRET=',
    'GITHUB_CLIENT_ID=',
    'GITHUB_CLIENT_SECRET=',
    `OAUTH_CALLBACK_BASE_URL=${domain ? `https://${domain}` : placeholder('oauth_callback_base_url')}`,
    '',
  ].join('\n');
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help === 'true') {
    process.stdout.write(`${usage()}\n`);
    return;
  }

  const outputPath = path.resolve(args.output || defaultOutputPath);
  if (fs.existsSync(outputPath) && args.force !== 'true') {
    throw new Error(
      `Refusing to overwrite existing env file without --force: ${outputPath}`
    );
  }

  const content = buildEnv(args);
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, `${content}\n`, 'utf8');

  const summary = {
    ok: true,
    output: outputPath,
    domainConfigured: isNonEmpty(args.domain),
    tlsEmailConfigured: isNonEmpty(args['tls-email']),
    dbHostConfigured: isNonEmpty(args['db-host']),
    redisHostConfigured: isNonEmpty(args['redis-host']),
    s3BucketConfigured: isNonEmpty(args['s3-bucket']),
    llmProvider: String(args['llm-provider'] || 'openai').trim().toLowerCase(),
    message:
      'Production bootstrap env written. Replace every REPLACE_ME_* value before go-live certification.',
  };

  process.stdout.write(`${JSON.stringify(summary, null, 2)}\n`);
}

try {
  main();
} catch (error) {
  process.stderr.write(`${error.message}\n`);
  process.exitCode = 1;
}
