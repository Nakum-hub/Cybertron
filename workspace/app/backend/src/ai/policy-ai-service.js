const PROMPT_VERSION = 'policy-v2';

function normalizePolicyKey(value) {
  const normalized = String(value || '').trim().toLowerCase();
  if (!normalized) {
    return 'security-policy';
  }
  return normalized.replace(/[^a-z0-9._:-]/g, '-').slice(0, 96) || 'security-policy';
}

function buildControlStatusAppendix(controls) {
  if (!Array.isArray(controls) || controls.length === 0) {
    return '\n### Mapped Controls\n- No mapped controls were supplied with this request.';
  }

  return `\n### Mapped Controls\n${controls
    .slice(0, 12)
    .map(control => `- ${control.controlId || control.control_id || 'N/A'}: ${control.status || 'not_started'}${control.notes ? ` | ${String(control.notes).slice(0, 180)}` : ''}`)
    .join('\n')}`;
}

