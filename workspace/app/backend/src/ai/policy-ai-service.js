function buildLocalPolicyDraft(policyKey, organization, controls) {
  const safeOrg = sanitizePromptInput(organization, 128);
  const template = POLICY_TEMPLATES[policyKey];
  if (template) {
    return template(safeOrg, controls);
  }

  const title = policyKey.replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
  return `# ${title} Policy

## 1. Purpose
This policy establishes the operational framework for ${policyKey.replace(/-/g, ' ')} at ${safeOrg}.

## 2. Scope
Applies to all systems, personnel, and processes within ${safeOrg} environments.

## 3. Control Statements
- Implement controls aligned with organizational risk appetite and compliance requirements.
- Document all procedures and maintain evidence of compliance.
- Assign ownership and accountability for each control area.
${buildControlStatusAppendix(controls)}

## 4. Monitoring
- Continuous monitoring through automated controls where feasible.
- Regular assessment of control effectiveness.
- Incident-driven reviews when control failures are detected.

## 5. Exceptions
All exceptions require management approval, compensating controls, and defined expiration dates.

## 6. Review Cadence
Annual review or upon significant organizational or regulatory change.

---
Generated by Cybertron Compliance Engine (Template) | ${new Date().toISOString().slice(0, 10)}`;
}

function buildPolicyFallbackResponse({ policyKey, organization, controls, reason, details, upstreamProvider, upstreamModel, attempts }) {
  return {
    policyKey,
    content: buildLocalPolicyDraft(policyKey, organization, controls),
    provider: 'local',
    model: reason ? 'quality-gated-fallback' : 'template',
    aiGenerated: false,
    approvalStatus: 'draft',
    requiresApproval: true,
    approvalNote: reason
      ? 'AI policy draft was withheld because it did not meet grounding and structure checks. Review this template-based draft before enforcement.'
      : 'This template-based policy draft must be reviewed and approved before enforcement. Output is rule-based, not AI-generated.',
    qualityGate: reason
      ? {
          accepted: false,
          attempts: attempts || 1,
          reasons: Array.isArray(details) ? details.slice(0, 10) : [String(reason)],
          upstreamProvider: upstreamProvider || null,
          upstreamModel: upstreamModel || null,
        }
      : undefined,
  };
}

async function generatePolicyDraft(config, log, payload = {}, context = {}) {
  const provider = createLlmProvider(config, log);
  const policyKey = normalizePolicyKey(payload.policyKey);
  const tenant = sanitizePromptInput(payload.tenant || 'global', 64);
  const organization = sanitizePromptInput(payload.organization || tenant, 128);
  const existingControls = Array.isArray(payload.controls) ? payload.controls : [];

  const sanitizedControls = existingControls.slice(0, 20).map(control => ({
    controlId: sanitizePromptInput(control.controlId || control.control_id, 32),
    status: sanitizePromptInput(control.status, 24),
    notes: sanitizePromptInput(control.notes, 500),
  }));

  if (!provider.isConfigured()) {
    log('warn', 'policy-ai.llm_not_configured', {
      message: 'LLM provider not configured, using template policy draft.',
      requestId: context.requestId,
    });

    return buildPolicyFallbackResponse({
      policyKey,
      organization,
      controls: existingControls,
    });
  }

  const userPrompt = [
    `Tenant: ${tenant}`,
    `Organization: ${organization}`,
    `Policy key: ${policyKey}`,
    'Generate an actionable policy document using these exact sections and headings:',
    'Purpose, Scope, Control Statements, Monitoring, Exceptions, Review Cadence, and Mapped Controls.',
    'Reference the exact mapped control IDs and current statuses from the input JSON.',
    'Do not cite regulations or standards that are not present in the input JSON.',
    'Return plain text only.',
    'Context JSON:',
    JSON.stringify(
      {
        controls: sanitizedControls,
      },
      null,
      2
    ),
  ].join('\n');

  try {
    const variant = selectPromptVariant(PROMPT_VERSION);
    const systemPrompt = variant
      ? hardenSystemPrompt(variant.systemPrompt)
      : hardenSystemPrompt(
          'You are a SOC2 compliance writer. Produce practical, audit-ready policy language for startup security operations.'
        );

    const startedAt = Date.now();
    const controlIds = sanitizedControls.map(control => control.controlId).filter(Boolean);
    const knownTerms = [
      policyKey.replace(/-/g, ' '),
      ...new Set(sanitizedControls.map(control => control.status).filter(Boolean)),
    ];

    const gatedResult = await generateTextWithQualityGate({
      provider,
      log,
      promptPayload: {
        systemPrompt,
        userPrompt,
        temperature: variant?.temperature ?? 0.2,
      },
      context,
      maxAttempts: 2,
      qualityContext: {
        requiredIds: controlIds,
        knownTerms,
      },
      evaluate: text => {
        const evaluation = assessLlmOutput(text, { policyKey, controls: sanitizedControls }, {
          requiredIds: controlIds,
          knownTerms,
          requiredHeadings: [
            'Purpose',
            'Scope',
            'Control Statements',
            'Monitoring',
            'Exceptions',
            'Review Cadence',
            'Mapped Controls',
          ],
          minGroundingScore: 60,
          minReferencedIds: controlIds.length > 1 ? 2 : Math.min(1, controlIds.length),
          minReferencedTerms: 1,
          minimumLength: 500,
          maxLength: 30_000,
        });

        const failureReasons = [...evaluation.failureReasons];
        if (controlIds.length > 0 && !/mapped controls/i.test(text)) {
          failureReasons.push('missing_mapped_controls_section');
        }

        return {
          ...evaluation,
          accepted: evaluation.accepted && failureReasons.length === 0,
          failureReasons,
        };
      },
    });

    const llmResult = gatedResult.llmResult;
    const groundingResult = gatedResult.evaluation?.grounding;

    if (!gatedResult.accepted) {
      return buildPolicyFallbackResponse({
        policyKey,
        organization,
        controls: existingControls,
        reason: 'grounding_failed',
        details: gatedResult.evaluation?.failureReasons,
        upstreamProvider: llmResult?.provider,
        upstreamModel: llmResult?.model,
        attempts: gatedResult.attempt,
      });
    }

    if (variant) {
      log('info', 'prompt_experiment', buildExperimentLogEntry({
        promptKey: PROMPT_VERSION,
        variantId: variant.variantId,
        requestId: context.requestId,
        tenantSlug: tenant,
        latencyMs: Date.now() - startedAt,
        groundingScore: groundingResult.score,
        parsedSuccessfully: true,
      }));
    }

    appendAuditLog(config, {
      tenantSlug: tenant,
      actorId: context.actorId || null,
      actorEmail: context.actorEmail || null,
      action: 'ai.policy.draft_generated',
      targetType: 'policy',
      targetId: policyKey,
      ipAddress: context.ipAddress || null,
      userAgent: context.userAgent || null,
      traceId: context.requestId || null,
      payload: {
        provider: llmResult.provider,
        model: llmResult.model,
        policyKey,
        approvalStatus: 'draft',
        groundingScore: groundingResult.score,
        qualityGateAccepted: true,
        qualityGateAttempts: gatedResult.attempt,
        promptVersion: PROMPT_VERSION,
      },
    }).catch(() => {});

    return {
      policyKey,
      content: gatedResult.evaluation.validation.text,
      provider: llmResult.provider,
      model: llmResult.model,
      aiGenerated: true,
      disclaimer: 'AI-generated draft policy. Must be reviewed and approved by authorized personnel before adoption.',
      promptVersion: PROMPT_VERSION,
      approvalStatus: 'draft',
      requiresApproval: true,
      approvalNote: 'This AI-generated policy draft must be reviewed, edited, and formally approved by authorized personnel before it can be enforced. Do not treat this as an active policy.',
      groundingScore: groundingResult.score,
      groundingDetails: groundingResult.ungroundedClaims.length > 0 ? groundingResult.ungroundedClaims : undefined,
      qualityGate: {
        accepted: true,
        attempts: gatedResult.attempt,
      },
    };
  } catch (error) {
    log('error', 'policy-ai.llm_request_failed', {
      message: error.message,
      requestId: context.requestId,
    });

    return {
      ...buildPolicyFallbackResponse({
        policyKey,
        organization,
        controls: existingControls,
        reason: 'llm_request_failed',
        details: [error.message],
      }),
      model: 'template-fallback',
      approvalNote: 'LLM request failed. This template-based policy draft must be reviewed and approved before enforcement. Output is rule-based, not AI-generated.',
    };
  }
}

